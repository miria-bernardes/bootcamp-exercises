package com.bootcamp.exercises.classtwo.classmanagement;

public class Estudante {

    private String nome;
    private int idade;
    private int matricula;
    private int serie;


    public Estudante(){}

    public Estudante(String nome, int idade, int matricula, int serie) {
        this.nome = nome;
        this.idade = idade;
        this.matricula = matricula;
        this.serie = serie;
    }
}
